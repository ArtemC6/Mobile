  static String languageNames(List<String> languageIds) {
    return languageIds
        .map((e) => languages.firstWhere((l) => l.id == e).name)
        .join(', ');
  }

  static String languageName(String languageId) {
    return languages
            .firstWhere(
              (l) => l.id == languageId,
              orElse: Language.new,
            )
            .name ??
        ''.toCapitalized();
  }

  static String platformLanguageId() {
    final loc = kIsWeb ? 'en' : Platform.localeName;
    final languageId = languages
        .firstWhere(
          (e) =>
              e.locale!.replaceFirst(RegExp('[-]'), '_') == loc ||
              e.locale!.substring(0, 2) == loc.substring(0, 2),
          orElse: Language.new,
        )
        .id;

    return languageId;
  }

  static Language platformLanguage() {
    final loc = kIsWeb ? 'en' : Platform.localeName;
    final language = languages.firstWhere(
      (e) =>
          e.locale!.replaceFirst(RegExp('[-]'), '_') == loc ||
          e.locale!.substring(0, 2) == loc.substring(0, 2),
      orElse: Language.new,
    );

    return language;
  }
